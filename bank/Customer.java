package bank;

import java.util.ArrayList;
import java.util.HashMap;

public class Customer {

	private String firstName = "", lastName = "", email = "", address = "", password = "";
	private long mobileNumber = 0, accountNumber = 0;
	private double balance = 500;
	private ArrayList<HashMap> transactions = new ArrayList<HashMap>();
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public long getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(long mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public long getAccountNumber() {
		return accountNumber;
	}
	public void setAccountNumber(long accountNumber) {
		this.accountNumber = accountNumber;
	}
	public double getBalance() {
		return balance;
	}
	public void setBalance(double balance) {
		this.balance = balance;
	}

	public ArrayList getTransactions() {
		return transactions;
	}

	public void setTransactions(HashMap transaction) {
		this.transactions.add(transaction);
	}
	
}

